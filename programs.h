extern CHIP8 chip8;

int current_program_index = 0;
uint16_t current_program_keys;

void load_program() {

  uint16_t addr = pgm_read_word((uint8_t*)programs + (sizeof(CHIP8_PROGRAM) * current_program_index));
  uint16_t size = pgm_read_word((uint8_t*)programs + (sizeof(CHIP8_PROGRAM) * current_program_index) + 2);
  current_program_keys = pgm_read_word((uint8_t*)programs + (sizeof(CHIP8_PROGRAM) * current_program_index) + 4);

	for (uint16_t i = 0; i < size; ++i) { 
		chip8.ram[CHIP8_PROGRAM_ADDR + i] = pgm_read_byte_near(addr + i);
	}
}

void next_program(uint8_t direction) {
  if (direction == 1) {

    if (current_program_index < (sizeof(programs) / sizeof(CHIP8_PROGRAM))-1) {
      current_program_index++;
    }
    else {
      current_program_index = 0;    
    }
  
  }
  else {
    
    if (current_program_index > 0) {
      current_program_index--;
    }
    else {
      current_program_index = (sizeof(programs) / sizeof(CHIP8_PROGRAM))-1;    
    }
    
  }

  chip8_reset_cpu(&chip8);
  load_program();
}
